#include <iostream>
using namespace std;

// Main function: Entry point of the program
int main() {
    // Variable Declaration
    int number;
    
    // Output: Prompting user
    cout << "Enter a number: ";
    
    // Input: Reading from user
    cin >> number;
    
    // Output: Displaying result
    cout << "You entered: " << number << endl;
    
    return 0; // Return 0 indicates successful execution
}
